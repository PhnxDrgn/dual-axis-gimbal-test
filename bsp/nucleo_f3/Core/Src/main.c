float map(float x, float in_min, float in_max, float out_min, float out_max)
{
  return (x - in_min) * (out_max - out_min) / (in_max - in_min) + out_min;
}

void enableServos()
{
  // pitch
  HAL_TIM_PWM_Start(&htim17, TIM_CHANNEL_1);
  
  // roll
  HAL_TIM_PWM_Start(&htim15, TIM_CHANNEL_2);
}

void disableServos()
{
  // pitch
  HAL_TIM_PWM_Stop(&htim17, TIM_CHANNEL_1);
  
  // roll
  HAL_TIM_PWM_Stop(&htim15, TIM_CHANNEL_2);
}

void setPitch(float angle)
{
  if (angle < SERVO_ANGLE_MIN)
    angle = SERVO_ANGLE_MIN;
  else if (angle > SERVO_ANGLE_MAX)
    angle = SERVO_ANGLE_MAX;

  htim17.Instance->CCR1 = (uint32_t)map(angle, SERVO_ANGLE_MIN, SERVO_ANGLE_MAX, 250.0, 1250.0); // 250 and 1250 are calculated from clock speed and ARR
}

void setRoll(float angle)
{
  if (angle < SERVO_ANGLE_MIN)
    angle = SERVO_ANGLE_MIN;
  else if (angle > SERVO_ANGLE_MAX)
    angle = SERVO_ANGLE_MAX;

  htim15.Instance->CCR2 = (uint32_t)map(angle, SERVO_ANGLE_MIN, SERVO_ANGLE_MAX, 250.0, 1250.0); // 250 and 1250 are calculated from clock speed and ARR
}

