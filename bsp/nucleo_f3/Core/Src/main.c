I2C_status_t hal2i2c(HAL_StatusTypeDef status)
{
  switch (status)
  {
  case HAL_OK:
    return I2C_OK;
  case HAL_BUSY:
    return I2C_BUSY;
  case HAL_TIMEOUT:
    return I2C_TIMEOUT;
  default:
    return I2C_ERROR;
  }
  return I2C_ERROR;
}

I2C_status_t I2C_rx(uint16_t devAddr, uint8_t reg, uint8_t *dataBuffer, uint16_t dataSize)
{
  uint16_t shiftedAddr = devAddr << 1;
  uint8_t data = reg;

  HAL_StatusTypeDef status = HAL_I2C_Master_Transmit(&hi2c1, shiftedAddr, &data, sizeof(data), I2C_REQ_TIMEOUT);

  if (status != HAL_OK)
    return hal2i2c(status);

  status = HAL_I2C_Master_Receive(&hi2c1, shiftedAddr, dataBuffer, dataSize, I2C_REQ_TIMEOUT);

  return hal2i2c(status);
}

I2C_status_t I2C_tx(uint16_t devAddr, uint8_t reg, uint8_t *dataBuffer, uint16_t dataSize)
{
  if (dataSize > I2C_BUFFER_SIZE)
    return I2C_ERROR;

  uint16_t shiftedAddr = devAddr << 1;
  uint8_t data[I2C_BUFFER_SIZE];

  data[0] = reg;

  memcpy(data + 1, dataBuffer, dataSize);

  HAL_StatusTypeDef status = HAL_I2C_Master_Transmit(&hi2c1, shiftedAddr, data, dataSize + 1, I2C_REQ_TIMEOUT);

  return hal2i2c(status);
}

